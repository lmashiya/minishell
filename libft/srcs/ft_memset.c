#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	size_t	i;
	char	*st;

	i = 0;
	st = (char *)s;
	while (i <= len)
	{
		st[i] = (char)c;
		i++;
	}
	return (st);
}
