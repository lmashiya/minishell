#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char		*str;
	int			i;

	str = ft_strnew(ft_strlen(s));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	return (str);
}
